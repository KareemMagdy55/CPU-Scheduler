public class Process {
    String name ;
    int color;

    int arrivalTime;
    int burstTime;
    int priorityNumber;
    int executionBeginTime;
    int finishingTime ;

    int processNum;
    public int getArrivalTime() {
        return arrivalTime;
    }

    public int getBurstTime() {
        return burstTime;
    }

    public int getPriorityNumber() {
        return priorityNumber;
    }

    public int getProcessNum() {
        return processNum;
    }
}
